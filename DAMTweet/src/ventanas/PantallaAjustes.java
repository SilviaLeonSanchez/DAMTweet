    private String pedirRutaInforme(){
        int resultado = jfc.showSaveDialog(this);
        if (resultado == JFileChooser.APPROVE_OPTION){
                return jfc.getSelectedFile().getAbsolutePath();
        }else{
            JOptionPane.showConfirmDialog(this, "No se ha indicado donde guardar el archivo por lo tanto no se generar√° el informe.", "Sin ruta especificada", JOptionPane.INFORMATION_MESSAGE);
            return null;
        }
    }
    
